#include<stdio.h>

int Display(int iValue1)
{
	if(iValue1 < 0)
    {
        iValue1 = -iValue1;
    }

	for(int i = 1; i <= iValue1; i++)
	{
		printf("%d\t",i);
	}
}

/////////////////////////////////////////////////////////////////////
// Write a program to perform Display Number
/////////////////////////////////////////////////////////////////////

int main()
{
	int iNo1 = 0;
	printf("Enter number: \n");
	scanf("%d",&iNo1);

	Display(iNo1);
	return 0;
}



